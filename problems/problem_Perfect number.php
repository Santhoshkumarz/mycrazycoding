public class Main
{
    public static void main(String [] args)
    {
        int n = 6,sum = 0;

        for(int i=1;i&ltn;i++) 
        {
            if(n%i==0)
            {
                sum = sum + i;
            }
        }

        if(sum==n)
        {
            System.out.print("Perfect number");
        }

        else
        {
            System.out.print("Not a Perfect number");
        }
    }
}
