<h1 id="problemhead">Abstract class in java</h1><br><br>

<p id="content_p">Abstract class used to declare and define the method.</p>
<br>
<p id="content_p">You can't define the abstract method in abstract class.  But you can define that abstract method in another class . That class should be extends the abstract class.</p>
<p id="content_p"></p>
<br><br><br>

<h2>Why Abstract Class use ?</h2><br><br>
<ol id="inside">
  <li>we don't want anyone to create object of abstract class.</li>
  <li>we can use one method which except all the subclass objects.</li>
</ol>

<br><br><br><hr><br><br><br>
<p id="content_p">The abstract class "Food" has a one abstract method. In that abstract method defined as child class.</p><br><br>
<div class="box">
  <div class="heading">Example-1</div>
    <div class="src">
    
<pre>
abstract class Food
{
      public abstract void Non_veg();
}

class A extends Food
{
      public void Non_veg()
      {
           System.out.println("Chicken");
      }
}

public class Main
{
      public static void main(String [] args)
      {   
           A obj = new A();

           obj.Non_veg();
      }
}
</pre>
    </div></div>
<br><br><br>

<h2>Output</h2><br><br>
    <div id="output">
    
<pre>
Chicken
</pre>
    </div><br><br><br><hr><br><br><br><br>
