<h1 id="problemhead">If else</h1><br><br>
<p id="content_p">If else statement is an decision making statement.It will execute the "if" block if the condition is true else it will execute the "else" block.</p>
    <br><br><br><h2>Syntax:</h2><br><br>
<div class="syntax">
<pre>
if condition:

  #statement

else:

  #statement
</pre>

    </div>    <br><br><br>
<div class="box">
  <div class="heading">Example</div>
    <div class="src">
<pre>
n = 5

if(n>0):

    print("Positive")

else:

    print("Negative")
</pre>
    </div></div>  <br>
    <br><h2>Output</h2><br><br>
   <div id="output">
    
<pre>
Positive
</pre>
    </div>
    <br><br><br><hr><br><br><br>



<h1 id="problemhead">Elif</h1><br><br>
<p id="content_p">Else-if statement is an decision making statement.else-if statement has two or more block of statement.which block of statement is true It will be executed.if all block of statement is false else block will be executed.</p>
<br><br><br>
<div class="box">
  <div class="heading">Example</div>

<div class="src">
<pre>
n = 5

if(n>0):

    print("Positive")

elif(n==0):

    print("Zero")

else:

    print("Negative")
</pre>
    </div></div>  <br>
    <br><h2>Output</h2><br><br>
    <div id="output">
    
<pre>
Positive
</pre>
    </div><br><br><br><hr><br><br><br>

<h1 id="problemhead">Nested if else</h1><br><br>
<p id="content_p">Nested if else statement is an decision making statement.It will give if statement inside the if statement.It will contain more than one condition.If the condition is true it will get inside the another if statement.If the condition is false it will execute the else block.</p>
<br><br><br>
    <h2>Syntax:</h2><br><br>
<div class="syntax">
<pre>
if(condition):

    if(condition):
       #statement
    else:
       #statement

else:
    #statement
</pre>

    </div>   <br><br><br>
<div class="box">
  <div class="heading">Example</div>

<div class="src">
<pre>
n = 7

if(n>0):

    if(n%2==0):
        print("even")
    else:
        print("odd")

else:

    print("Negative")
</pre>
    </div> </div> <br><br>
    <h2>Output</h2><br><br>
<div id="output">
    
<pre>
odd
</pre>
    </div>




