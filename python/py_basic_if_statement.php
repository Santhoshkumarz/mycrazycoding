<h1 id="problemhead">If statement in python</h1><br><br>
<p id="content_p">If statement is an dicision making statement.It will execute the "if" statement if the condition is true.</p>
	  <br><br><br><h2>Syntax:</h2><br><br>
	  
<div class="syntax">
<pre>
if condition:

  #statement
</pre>

	  </div>    <br> <br><br>
