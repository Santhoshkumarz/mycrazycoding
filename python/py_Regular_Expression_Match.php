<h1 id="problemhead">Match in python</h1><br><br>
<p id="content_p"> "match" is one of the function of "re" module.  This function is used for searching in strings in very fast and efficient manner. While searching it will return when the "match" object is found at the starting of the string otherwise it will return none.</p>


<br><br><br><hr><br><br><br>

