<h1 id="problemhead">Primary key in SQL</h1><br><br>



<p id="content_p">The Primary key must contain only unique values. It can not allow duplicate values. The table allow  only one primary key.</p>




<br><br><br>


<h2>Syntax:</h2><br><br>


<div class="box">
  <div class="heading">MySQL</div>
    <div class="src">
<pre>
CREATE TABLE tablename
{
    column_name datatype NOT NULL,

    PRIMARY KEY (column_name)
}
</pre>
    </div></div>

<br><br>


<div class="box">
  <div class="heading">SQL Server / Oracle / MS Access</div>
    <div class="src">
<pre>
CREATE TABLE tablename
{
    column_name datatype NOT NULL PRIMARY KEY
}
</pre>
    </div></div>

	  <br><br><br><hr><br><br><br>










<div class="box">
  <div class="heading">Example-1</div>
	  <div class="src">
	  
<pre>
CREATE TABLE employee(

    Name VARCHAR(20) NOT NULL,
    
    AccountNumber VARCHAR(20) NOT NULL,
    
    PRIMARY KEY(AccountNumber) 

);

INSERT INTO employee VALUES ("Tony","1234");

INSERT INTO employee VALUES ("Natasha","1235");

INSERT INTO employee VALUES ("Steve","1236");

SELECT * FROM employee;
</pre>
	  </div></div>
